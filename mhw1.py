import numpy as np

M = np.array([[100, 150, 200],
              [50,  100, 150],
              [0,   50,  100]])

E = np.array([[20, 30, 40],
              [10, 20, 30],
              [5,  10, 15]])

# 1. Зміна контрасту
print("=" * 30)
print("1. Зміна контрасту (0.5 × M):")
print(M * 0.5)

# 2. Корекція яскравості
print("=" * 30)
print("2. Корекція яскравості (M + 25):")
print(M + 25)

# 3. Blending
print("=" * 30)
print("3. Blending (0.8×M + 0.2×E):")
print(0.8 * M + 0.2 * E)